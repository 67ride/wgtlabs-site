function setLanguage(lang) {
  const dictionary = translations[lang] || translations.cs;
  document.documentElement.lang = lang;
  document.documentElement.dataset.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) element.textContent = dictionary[key];
  });

  document.querySelectorAll("[data-lang-switch]").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.langSwitch === lang);
  });

  localStorage.setItem("wgtlabs-language", lang);
}

document.querySelectorAll("[data-lang-switch]").forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.langSwitch));
});

const savedLanguage = localStorage.getItem("wgtlabs-language");
const browserLanguage = navigator.language && navigator.language.toLowerCase().startsWith("cs") ? "cs" : "en";
setLanguage(savedLanguage || browserLanguage || "cs");


// v13 motion layer: reveal-on-scroll + subtle pointer glow
const motionTargets = [
  ".hero",
  ".hero-main > h1",
  ".hero-main > .lead",
  ".hero-points div",
  ".hero-actions",
  ".signal-panel",
  ".who-section",
  ".who-rail",
  ".who-copy",
  ".project-section",
  ".project-logo-box",
  ".project-intro",
  ".status-card",
  ".skills-section",
  ".skill-card",
  ".site-footer"
];

const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;

window.addEventListener("load", () => {
  document.body.classList.add("is-loaded");
});

if (!reduceMotion) {
  const revealElements = document.querySelectorAll(motionTargets.join(","));
  revealElements.forEach((element) => element.classList.add("reveal"));

  const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        revealObserver.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12, rootMargin: "0px 0px -8% 0px" });

  revealElements.forEach((element) => revealObserver.observe(element));

  const cursorGlow = document.querySelector(".cursor-glow");
  if (cursorGlow) {
    window.addEventListener("pointermove", (event) => {
      cursorGlow.style.left = `${event.clientX}px`;
      cursorGlow.style.top = `${event.clientY}px`;
    }, { passive: true });
  }
}
